package cornucopia.entity;

public class OrderEntity {
	private String orderNamePre;
	private int num;
	private String newOrderNo;

	public String getOrderNamePre() {
		return orderNamePre;
	}

	public void setOrderNamePre(String orderNamePre) {
		this.orderNamePre = orderNamePre;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getNewOrderNo() {
		return newOrderNo;
	}

	public void setNewOrderNo(String newOrderNo) {
		this.newOrderNo = newOrderNo;
	}
}
